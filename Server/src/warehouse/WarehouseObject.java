package warehouse;

public class WarehouseObject {

    private int x, y;

    WarehouseObject(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String toString() {
        return "-";
    }
}
